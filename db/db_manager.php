<?php

const SERVER_NAME = "localhost";
const USER_NAME = "root";
const PASSWORD = "root";
const DB_NAME = "aln_review";

/**
 * 
 * @return PDO | null
 */
function connectDB() {
    try {
        $connectDBObj = new PDO("mysql:host=" . SERVER_NAME . ";dbname=" . DB_NAME, USER_NAME, PASSWORD);
        // set the PDO error mode to exception
        $connectDBObj->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
        echo "Connected successfully";
        $szTableCreatingText = getSQLCommandFromFile('db/table_creator.sql');
        $connectDBObj->exec($szTableCreatingText);
        return $connectDBObj;
    } catch (PDOException $e) {
        echo "Connection failed: " . $e->getMessage();
        return null;
    }
}

function selectData($connectDBObj, $szSql) {
    $stmt = $connectDBObj->prepare($szSql);
    $stmt->execute();
    $stmt->setFetchMode(PDO::FETCH_ASSOC);
    return $stmt->fetchAll();
}

function getSQLCommandFromFile($szFileName) {
    $szFileContent = "";
    $myFile = fopen($szFileName, "r") or die("Unable to open file " . $szFileName . "!");
    $szFileContent = fread($myFile, filesize($szFileName));
    fclose($myFile);
    return $szFileContent;
}

